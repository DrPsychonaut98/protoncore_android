package me.proton.core.payment.presentation

/**
 * The expiration year UI is usually 2 digits. If that is the case, this function will check and fix it to 4 digit year.
 */
internal fun String.adjustExpirationYear() = takeUnless { length == 2 } ?: "20$this"
